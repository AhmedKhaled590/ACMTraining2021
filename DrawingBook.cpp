/*
 * Complete the pageCount function below.
 */
int pageCount(int n, int p)
{
    int front = -1, back = -1;
    if (n % 2 != 0)
    {
        front = floor(p / 2.0);
        back = floor((n - p) / 2.0);
    }
    else
    {
        front = floor(p / 2.0);
        back = ceil((n - p) / 2.0);
    }
    return min(front, back);
}

int main()
{
    ofstream fout(getenv("OUTPUT_PATH"));

    int n;
    cin >> n;
    cin.ignore(numeric_limits<streamsize>::max(), '\n');

    int p;
    cin >> p;
    cin.ignore(numeric_limits<streamsize>::max(), '\n');

    int result = pageCount(n, p);

    fout << result << "\n";

    fout.close();

    return 0;
}
